# 1.  ROUGE evaluation (extractive summaries, Pipeline 1 / DistilBERT)

def evaluate_summaries(
    refs: list[str],
    gens: list[str],
) -> pd.DataFrame:
    """
    Compute per-pair ROUGE-1, ROUGE-2, and ROUGE-L scores between reference
    and generated summaries using stemmed matching.

    Empty pairs (either ref or gen is blank after stripping) are scored as
    all-zero to avoid dividing by zero inside the scorer.

    Parameters
    ----------
    refs : list[str]
        Reference oracle summaries (one string per document).
    gens : list[str]
        Generated/predicted summaries (one string per document).

    Returns
    -------
    pd.DataFrame
        DataFrame with nine columns:
        ``rouge1_p``, ``rouge1_r``, ``rouge1_f``,
        ``rouge2_p``, ``rouge2_r``, ``rouge2_f``,
        ``rougeL_p``, ``rougeL_r``, ``rougeL_f``.
        One row per (ref, gen) pair.
    """
    scorer = rouge_scorer.RougeScorer(
        ["rouge1", "rouge2", "rougeL"], use_stemmer=True
    )
    results: list[dict[str, float]] = []

    for ref, gen in zip(refs, gens):
        ref = ref.strip()
        gen = gen.strip()

        if ref and gen:
            score = scorer.score(ref, gen)
            results.append(
                {
                    "rouge1_p": score["rouge1"].precision,
                    "rouge1_r": score["rouge1"].recall,
                    "rouge1_f": score["rouge1"].fmeasure,
                    "rouge2_p": score["rouge2"].precision,
                    "rouge2_r": score["rouge2"].recall,
                    "rouge2_f": score["rouge2"].fmeasure,
                    "rougeL_p": score["rougeL"].precision,
                    "rougeL_r": score["rougeL"].recall,
                    "rougeL_f": score["rougeL"].fmeasure,
                }
            )
        else:
            results.append(
                {
                    "rouge1_p": 0.0, "rouge1_r": 0.0, "rouge1_f": 0.0,
                    "rouge2_p": 0.0, "rouge2_r": 0.0, "rouge2_f": 0.0,
                    "rougeL_p": 0.0, "rougeL_r": 0.0, "rougeL_f": 0.0,
                }
            )

    return pd.DataFrame(results)


def evaluate_model(
    dataset_name: str,
    model_fn: Any,
    drive_path: str,
    save_summaries: bool = False,
    test_size: int = 2500,
) -> pd.DataFrame:
    """
    Evaluate extractive summaries generated by *model_fn* against oracle
    summaries on the test split of *dataset_name*.

    The function:
    1. Loads and pre-processes the test split via
       :func:`load_and_preprocess_test_dataset`.
    2. Generates a summary for each document using *model_fn* (which may
       optionally persist the summary to disk when ``save_summaries=True``).
    3. Computes ROUGE scores via :func:`evaluate_summaries`.

    Parameters
    ----------
    dataset_name : str
        One of ``"arxiv_enriched"`` or ``"pubmed_enriched"``.
    model_fn : callable
        Callable ``(text: str, [summary_id: str]) → str``.
        When *save_summaries* is ``True``, it is called with ``summary_id``
        (a zero-based string index) so the function can persist results.
    drive_path : str
        Project root directory (used to resolve dataset paths).
    save_summaries : bool
        Whether to pass a ``summary_id`` to *model_fn* (default: False).
    test_size : int
        Number of test examples to evaluate (default: 2500).

    Returns
    -------
    pd.DataFrame
        ROUGE scores DataFrame (see :func:`evaluate_summaries`).
    """
    dataset = load_and_preprocess_test_dataset(dataset_name, drive_path)
    dataset = dataset.shuffle(seed=42).select(range(min(test_size, len(dataset))))

    print("Generating summaries …")
    generated: list[str] = []
    references: list[str] = []

    for idx, example in enumerate(tqdm(dataset, desc="Inference")):
        text_input: str = (
            " ".join(example["text"])
            if isinstance(example["text"], list)
            else example["text"]
        )

        if save_summaries:
            summary: str = model_fn(text_input, summary_id=str(idx + 1))
        else:
            summary = model_fn(text_input)

        generated.append(summary)

        raw_oracle = example.get("oracle_summary", "")
        oracle_str: str = (
            " ".join(raw_oracle) if isinstance(raw_oracle, list) else raw_oracle
        )
        oracle_sents = sent_tokenize(oracle_str)
        references.append(" ".join(oracle_sents))

    return evaluate_summaries(references, generated)


def evaluate_extractive_distilbert(
    trainer: Any,
    tokenizer: Any,
    raw_ds: Any,
    output_dir: str,
    split: str = "test",
    k: int = 5,
) -> dict[str, float]:
    """
    Run inference with a trained DistilBERT sentence-level classifier and
    compute ROUGE scores against oracle summaries.

    Top-*k* sentences (by classifier probability for the positive class) are
    joined in their original document order to form the extractive summary.

    Predictions and scores are saved to *output_dir* as:
    * ``<split>_predictions.jsonl``
    * ``<split>_rouge.json``

    Parameters
    ----------
    trainer : Trainer
        HuggingFace Trainer holding the trained DistilBERT model.
    tokenizer : DistilBertTokenizerFast
        Matching tokenizer for inference.
    raw_ds : DatasetDict
        Document-level dataset (with ``"text"`` and ``"labels"`` string
        columns).
    output_dir : str
        Directory for saving predictions and metric JSON files.
    split : str
        Dataset split to evaluate (default: ``"test"``).
    k : int
        Number of top sentences to select (default: 5).

    Returns
    -------
    dict[str, float]
        Average ROUGE-1 / ROUGE-2 / ROUGE-L F1 scores.
    """
    import torch

    rouge_metric = rouge_scorer.RougeScorer(
        ["rouge1", "rouge2", "rougeL"], use_stemmer=True
    )
    model = trainer.model.to(trainer.args.device)
    preds_list: list[str] = []
    refs_list:  list[str] = []

    for doc in tqdm(raw_ds[split], desc=f"DistilBERT eval [{split}]"):
        sents = sent_tokenize(doc["text"])
        enc = tokenizer(
            sents,
            padding=True,
            truncation=True,
            return_tensors="pt",
        ).to(model.device)

        with torch.no_grad():
            logits = model(**enc).logits
            probs  = torch.softmax(logits, dim=-1)[:, 1].cpu().numpy()

        top_idxs = np.argsort(probs)[-k:]
        pred = " ".join([sents[i] for i in sorted(top_idxs)])
        preds_list.append(pred)
        refs_list.append(doc["labels"])

    scores = rouge_metric
    results: list[dict[str, float]] = []
    for pred, ref in zip(preds_list, refs_list):
        sc = rouge_metric.score(ref, pred)
        results.append(
            {
                "rouge1_f": sc["rouge1"].fmeasure,
                "rouge2_f": sc["rouge2"].fmeasure,
                "rougeL_f": sc["rougeL"].fmeasure,
            }
        )

    df = pd.DataFrame(results)
    avg_scores: dict[str, float] = df.mean().round(4).to_dict()
    print(f"\nROUGE scores on [{split}]: {avg_scores}")

    os.makedirs(output_dir, exist_ok=True)
    save_jsonl(
        [{"prediction": p, "reference": r} for p, r in zip(preds_list, refs_list)],
        os.path.join(output_dir, f"{split}_predictions.jsonl"),
    )
    save_json(avg_scores, os.path.join(output_dir, f"{split}_rouge.json"))
    print(f"Saved predictions → {output_dir}/{split}_predictions.jsonl")
    print(f"Saved scores      → {output_dir}/{split}_rouge.json")

    return avg_scores


# 2.  BERTScore

def compute_bertscore(
    refs: list[str],
    hyps: list[str],
) -> dict[str, float]:
    """
    Compute corpus-level BERTScore precision, recall, and F1 between
    *hyps* (hypotheses) and *refs* (references).

    Parameters
    ----------
    refs : list[str]
        Reference summary strings.
    hyps : list[str]
        Hypothesis (generated) summary strings.

    Returns
    -------
    dict[str, float]
        ``{"precision-bertscore": …, "recall-bertscore": …, "f1-bertscore": …}``
    """
    precision, recall, f1 = bert_score_fn(hyps, refs, lang="en", verbose=False)
    return {
        "precision-bertscore": float(precision.mean()),
        "recall-bertscore":    float(recall.mean()),
        "f1-bertscore":        float(f1.mean()),
    }


# 3.  GPT-based FactCC hallucination checker

def compute_factcc_via_gpt(
    refs: list[str],
    hyps: list[str],
    model: str = "gpt-4o-mini",
    temperature: float = GPT_TEMPERATURE_BASELINE,
) -> dict[str, float]:
    """
    Estimate factual consistency by querying GPT for each (reference,
    hypothesis) pair.

    For each pair, GPT is asked to determine whether the *reference* (premise)
    supports the *hypothesis* and to output a JSON object with:

    * ``"label"`` — ``"SUPPORTS"`` or ``"NOT_SUPPORTS"``
    * ``"score"`` — a float in ``[0, 1]`` expressing confidence

    The final FactCC score is the mean of:
    * *score* when ``label == "SUPPORTS"``
    * *(1 − score)* when ``label == "NOT_SUPPORTS"``

    This formulation means higher values always indicate greater factual
    support.

    Parameters
    ----------
    refs : list[str]
        Ground-truth reference summaries (used as premises).
    hyps : list[str]
        Generated hypothesis summaries to check.
    model : str
        OpenAI model to use (default: ``"gpt-4o-mini"``).
    temperature : float
        Sampling temperature (default: 0.5 for slight diversity in reasoning).

    Returns
    -------
    dict[str, float]
        ``{"factcc_score": <mean_factcc>}``
    """
    from src.models import call_openai_chat

    total_score = 0.0

    for ref, hyp in zip(refs, hyps):
        prompt: list[dict[str, str]] = [
            {
                "role": "system",
                "content": (
                    "You are a fact-consistency checker. First list reasons why "
                    "the premise may support or not support the hypothesis, then "
                    "output JSON."
                ),
            },
            {
                "role": "user",
                "content": (
                    f"Premise:\n{ref}\n\nHypothesis:\n{hyp}\n\n"
                    "Answer with valid JSON: keys: label (SUPPORTS or NOT_SUPPORTS), "
                    "score (0–1)."
                ),
            },
        ]
        response = call_openai_chat(
            prompt,
            max_tokens=200,
            temperature=temperature,
            model=model,
        )
        try:
            obj = json.loads(response)
            label: str  = obj.get("label", "")
            score: float = float(obj.get("score", 0.0))
            total_score += score if label == "SUPPORTS" else (1.0 - score)
        except json.JSONDecodeError:
            # Treat unparseable responses as zero contribution.
            total_score += 0.0

    avg = total_score / len(refs) if refs else 0.0
    return {"factcc_score": float(avg)}


# 4.  Summary I/O for the abstractive rewrite pipeline (Pipeline 2)

def load_reference_summaries(path: str) -> dict[int, str]:
    """
    Read a JSONL file produced by the original dataset and build an index-to-
    summary mapping.

    Each line is expected to have a ``"summary"`` key whose value is a list of
    sentence strings.  The sentences are joined with spaces to form a single
    string.

    Parameters
    ----------
    path : str
        Path to the ground-truth ``.jsonl`` file (e.g. ``test.jsonl``).

    Returns
    -------
    dict[int, str]
        Mapping ``{line_index: summary_string}``.
    """
    refs: dict[int, str] = {}
    with open(path, "r", encoding="utf-8") as fh:
        for idx, line in enumerate(fh):
            record = json.loads(line)
            refs[idx] = " ".join(record["summary"])
    return refs


def load_generated_summaries(dirpath: str) -> dict[int, str]:
    """
    Read all ``*.txt`` files in *dirpath* whose stem is an integer and return
    a stem-index → text mapping.

    Parameters
    ----------
    dirpath : str
        Directory containing numbered ``.txt`` summary files.

    Returns
    -------
    dict[int, str]
        Mapping ``{file_stem_int: summary_text}``.
    """
    gens: dict[int, str] = {}
    for p in Path(dirpath).glob("*.txt"):
        if p.stem.isdigit():
            gens[int(p.stem)] = p.read_text(encoding="utf-8").strip()
    return gens


# 5.  Test-set dataset loading (Pipeline 1)

def _preprocess_test_example(example: dict[str, Any]) -> dict[str, Any]:
    """
    Normalise a single raw test record: join list fields into strings, handle
    missing oracle summaries, and strip unwanted auxiliary keys.

    Parameters
    ----------
    example : dict
        A single parsed JSONL record.

    Returns
    -------
    dict
        Cleaned record ready for HuggingFace Dataset construction.
    """
    from src.utils import DROP_KEYS

    if isinstance(example.get("text"), list):
        example["text"] = " ".join(example["text"])
    if isinstance(example.get("summary"), list):
        example["summary"] = " ".join(example["summary"])

    raw_oracle = example.get("oracle_summary")
    if raw_oracle is None:
        example["oracle_summary"] = ""
    elif isinstance(raw_oracle, list):
        example["oracle_summary"] = " ".join(raw_oracle)
    else:
        example["oracle_summary"] = str(raw_oracle).strip()

    for key in ["indices", "sorted_indices", "score"]:
        example.pop(key, None)

    return example


def load_and_preprocess_test_dataset(
    dataset_name: str,
    drive_path: str,
) -> Dataset:
    """
    Load the raw ``test.jsonl`` file for *dataset_name*, apply text
    normalisation, and return a HuggingFace Dataset with a dynamically
    inferred feature schema.

    Parameters
    ----------
    dataset_name : str
        One of ``"arxiv_enriched"`` or ``"pubmed_enriched"``.
    drive_path : str
        Project root directory.

    Returns
    -------
    Dataset
        Preprocessed test dataset.
    """
    from datasets import Features, Sequence, Value

    test_file = os.path.join(
        drive_path, "dataset", dataset_name, "test.jsonl"
    )
    records: list[dict[str, Any]] = []
    with open(test_file, "r", encoding="utf-8") as fh:
        for line in fh:
            ex = json.loads(line)
            ex = _preprocess_test_example(ex)
            records.append(ex)

    if not records:
        return Dataset.from_dict({})

    columnar: dict[str, list] = {
        key: [ex[key] for ex in records] for key in records[0].keys()
    }

    # Dynamically infer Features schema from the first record.
    features_dict: dict[str, Any] = {}
    for key, value in records[0].items():
        if isinstance(value, list):
            elem = value[0] if value else None
            if isinstance(elem, int):
                features_dict[key] = Sequence(Value("int64"))
            else:
                features_dict[key] = Sequence(Value("string"))
        elif isinstance(value, str):
            features_dict[key] = Value("string")
        elif isinstance(value, int):
            features_dict[key] = Value("int64")

    return Dataset.from_dict(columnar, features=Features(features_dict))


# 6.  Master evaluation entry point (Pipeline 2 abstractive summaries)

def evaluate_all(base_dir: str, datasets: list[str] | None = None) -> None:
    """
    Master evaluation function for the abstractive rewrite pipeline (Pipeline 2).

    For each dataset in *datasets*, this function:
    1. Loads the gold reference summaries from ``dataset/<ds>/test.jsonl``.
    2. Loads the generated abstractive summaries from
       ``abs_summaries/<ds>/test_summaries/*.txt``.
    3. Aligns summaries by common integer indices.
    4. Computes BERTScore and GPT-based FactCC.
    5. Saves results to ``abs_summaries/<ds>/evaluation_results.json``.

    Parameters
    ----------
    base_dir : str
        Project root directory.
    datasets : list[str] | None
        Datasets to evaluate.  Defaults to :data:`src.utils.DATASETS` if not
        provided.
    """
    import logging

    logger = logging.getLogger(__name__)

    if datasets is None:
        datasets = DATASETS

    for dataset in datasets:
        logger.info("Evaluating dataset: %s", dataset)

        test_path = os.path.join(base_dir, "dataset", dataset, "test.jsonl")
        gen_dir   = os.path.join(base_dir, "abs_summaries", dataset, "test_summaries")
        out_path  = os.path.join(base_dir, "abs_summaries", dataset, "evaluation_results.json")
        os.makedirs(os.path.dirname(out_path), exist_ok=True)

        ref_dict = load_reference_summaries(test_path)
        gen_dict = load_generated_summaries(gen_dir)

        common_ids: list[int] = sorted(set(ref_dict.keys()) & set(gen_dict.keys()))
        if not common_ids:
            raise RuntimeError(
                f"No matching summary IDs between {test_path} and {gen_dir}"
            )

        logger.info("[%s] Matched IDs: %s", dataset, common_ids)

        aligned_refs: list[str] = [ref_dict[i] for i in common_ids]
        aligned_hyps: list[str] = [gen_dict[i] for i in common_ids]
        logger.info("[%s] Aligned %d example(s)", dataset, len(aligned_refs))

        results: dict[str, float] = {}
        results.update(compute_bertscore(aligned_refs, aligned_hyps))
        results.update(compute_factcc_via_gpt(aligned_refs, aligned_hyps))

        save_json(results, out_path)
        logger.info("Results for %s:\n%s", dataset, json.dumps(results, indent=2))
